 * 校验金额格式
 * @param  [type] $accountPrice 金额值
 * @return [type]               [description]
 */
function check_money_format($accountPrice)    
{    
    if (!preg_match('/^[0-9]+(.[0-9]{1,2})?$/', $accountPrice)) return false;    
    return true;    
} 