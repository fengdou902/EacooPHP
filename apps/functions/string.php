    //提取前17位的其中一位，并将变量类型转为实数
    $b = (int) $number{$i};

    //提取相应的加权因子
    $w = $wi[$i];

    //把从身份证号码中提取的一位数字和加权因子相乘，并累加
    $sigma += $b * $w;
   }
   //计算序号
   $snumber = $sigma % 11;

   //按照序号从校验码串中提取相应的字符。
   $check_number = $ai[$snumber];

   if ($number{17} == $check_number) {
    return true;
   } else {
    return false;
   }
}

